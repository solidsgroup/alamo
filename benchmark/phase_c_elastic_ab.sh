#!/usr/bin/env bash
#
# phase_c_elastic_ab.sh -- Phase-C elastic-solver A/B attribution (256^3, 1 A100).
#
# Roadmap: GPU_ROADMAP_V2.md Phase C / benchmark/PHASE_A_FINDINGS.md.
# Phase A proved the combined flame+elastic workload is ~95% elastic on GPU and
# Operator::Elastic::Fapply is 74.6% of GPU kernel time.  This script measures
# how much each ZERO-SOURCE input lever buys, so we know where the structural
# (source) work is actually needed.
#
# Each variant is a SHORT run (stop_time small => ~6-12 elastic solves) with
# elastic.solver.verbose=4 so the per-solve V-cycle/Newton ladder and the
# per-solve wall time are captured.  Compare with benchmark/compare_tinyprofiler.py
# (TinyProfiler MLMG::solve incl. time) and benchmark/compare_thermo.py (stress
# parity -- a lever is only valid if the stress field still matches the baseline).
#
# Variants (all 256^3, 1xA100, identical physics):
#   baseline    input_3d_centre_bore_256_a2                     (tol 1e-8, interval 50, bicgstab uncapped)
#   tol         baseline + tol_rel/abs=1e-6                     (isolates outer-tolerance lever, B1)
#   bottom      baseline + bottom_tol_rel=1e-4 max_iter=50      (isolates bottom-cap lever, B3)
#   interval    baseline + elastic.interval=100                 (isolates solve-frequency lever, B2)
#   tuned       input_3d_centre_bore_256_a2_tuned               (all B-levers stacked)
#
# Like phase_a2_sweep.sh: dry-run by default (emits the sbatch matrix to $OUT);
# pass --submit on a SLURM host to actually submit.
#
# Flags:   --submit | --dry-run | --scavenger
# Env:     GPU_TYPE=a100  GPU_NODE_CPUS=8  GPU_MEM=64G  STOP_TIME=0.06  PARTITION=nova
#          OUT=phase_c_ab_commands.txt
#
set -euo pipefail

GPU_TYPE="${GPU_TYPE:-a100}"
GPU_NODE_CPUS="${GPU_NODE_CPUS:-8}"
GPU_MEM="${GPU_MEM:-64G}"
STOP_TIME="${STOP_TIME:-0.06}"          # 0.06 s / 1e-4 = 600 steps => ~12 solves @interval=50
PARTITION="${PARTITION:-nova}"
OUT="${OUT:-phase_c_ab_commands.txt}"
SCRIPT_GPU="benchmark/nova_flame_gpu_3d_a2.slurm"
BASE_INPUT="input_3d_centre_bore_256_a2"
TUNED_INPUT="input_3d_centre_bore_256_a2_tuned"

SUBMIT=0
for arg in "$@"; do
    case "$arg" in
        --submit)    SUBMIT=1 ;;
        --dry-run)   SUBMIT=0 ;;
        --scavenger) PARTITION=scavenger ;;
        -h|--help)   sed -n '2,33p' "$0" | sed 's/^# \{0,1\}//'; exit 0 ;;
        *) echo "error: unknown argument '$arg' (try --help)" >&2; exit 2 ;;
    esac
done

# Common short+verbose overrides for every variant.
COMMON_EXTRA="stop_time=${STOP_TIME} elastic.solver.verbose=4 amr.plot_int=-1"

# variant name | input file | per-variant EXTRA_ARGS (appended last, last key wins)
VARIANTS=(
  "baseline|${BASE_INPUT}|"
  "tol|${BASE_INPUT}|elastic.tol_rel=1e-6 elastic.tol_abs=1e-6"
  "bottom|${BASE_INPUT}|elastic.solver.bottom_tol_rel=1e-4 elastic.solver.bottom_tol_abs=0.0 elastic.solver.bottom_max_iter=50"
  "interval|${BASE_INPUT}|elastic.interval=100"
  "tuned|${TUNED_INPUT}|"
)

echo "============================================================"
echo " Phase-C elastic A/B attribution (256^3, 1x${GPU_TYPE})"
echo "   STOP_TIME = ${STOP_TIME}   PARTITION = ${PARTITION}"
echo "   ACTION    = $([[ $SUBMIT -eq 1 ]] && echo SUBMIT || echo 'DRY RUN')"
echo "============================================================"

: > "$OUT"
{
  echo "# Phase-C elastic A/B command matrix (generated by phase_c_elastic_ab.sh)"
  echo "# GPU_TYPE=$GPU_TYPE PARTITION=$PARTITION STOP_TIME=$STOP_TIME"
  echo "# compare: benchmark/compare_tinyprofiler.py (MLMG::solve incl), benchmark/compare_thermo.py (stress parity)"
  echo "#"
} >> "$OUT"

sbatch_opts=(--partition="${PARTITION}" --nodes=1 --gres="gpu:${GPU_TYPE}:1" \
             --ntasks=1 --ntasks-per-node=1 --cpus-per-task="${GPU_NODE_CPUS}" --mem="${GPU_MEM}" \
             --time=01:00:00 -J flame_gpu_3d_phaseC)

for v in "${VARIANTS[@]}"; do
    IFS='|' read -r name input extra <<< "$v"
    extra_args="${COMMON_EXTRA}${extra:+ $extra}"
    opts_text=""; printf -v opts_text ' %q' "${sbatch_opts[@]}"
    cmd="INPUT=${input} GPU_TYPE=${GPU_TYPE} MODE=bench PARTITION=${PARTITION} EXTRA_ARGS=$(printf '%q' "${extra_args}") sbatch${opts_text} ${SCRIPT_GPU}"
    printf '# variant=%s  input=%s\n%s\n\n' "$name" "$input" "$cmd" >> "$OUT"
    if [[ "$SUBMIT" -eq 1 ]]; then
        if command -v sbatch >/dev/null 2>&1; then
            echo "submitting variant=${name}"
            INPUT="${input}" GPU_TYPE="${GPU_TYPE}" MODE=bench PARTITION="${PARTITION}" \
                EXTRA_ARGS="${extra_args}" sbatch "${sbatch_opts[@]}" "${SCRIPT_GPU}"
        else
            echo "WARN: --submit but no sbatch; skipping variant=${name}" >&2
        fi
    fi
done

echo
echo "Wrote command matrix to: $OUT"
[[ "$SUBMIT" -eq 0 ]] && echo "(dry run -- nothing submitted; re-run with --submit on a SLURM host)"
echo
echo "After runs complete, attribute per-solve cost from each .out:"
echo "  grep -E 'MLMG::solve|oneIter|Fapply|ELASTIC SOLVE' flame_gpu_3d_phaseC.<jobid>.out"
echo "  python3 benchmark/compare_thermo.py output_3d_centre_bore_256_a2*/   # stress parity gate"
